export const dictionaries: Record<AppLocale, Dict> = { en, es };

function deepGet(dict: Dict, path: string): string | undefined {
  const parts = path.split(".");
  let cur: string | Dict | undefined = dict;
  for (const p of parts) {
    if (cur === undefined || typeof cur === "string") return undefined;
    cur = cur[p] as string | Dict | undefined;
  }
  return typeof cur === "string" ? cur : undefined;
}

export function translate(
  locale: AppLocale,
  path: string,
  vars?: Record<string, string | number>,
): string {
  let s =
    deepGet(dictionaries[locale], path) ??
    deepGet(dictionaries.en, path) ??
    path;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replaceAll(`{${k}}`, String(v));
    }
  }
  return s;
}
